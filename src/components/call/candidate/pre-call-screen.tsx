"use client";

import { zodResolver } from "@hookform/resolvers/zod";
import { AlarmClockIcon } from "lucide-react";
import Image from "next/image";
import { Controller, useForm } from "react-hook-form";
import { z } from "zod";
import { Field, FieldContent, FieldError, FieldGroup } from "@/components/ui/field";
import { Input } from "@/components/ui/input";
import type { Interview } from "@/types/interview";
import MiniLoader from "@/components/loaders/mini-loader/miniLoader";
import { Button } from "@/components/ui/button";
import { Card } from "@/components/ui/card";
import { ConfirmDialog } from "./confirm-dialog";

const formSchema = z.object({
  email: z.string().email("Please enter a valid email"),
  firstName: z.string().min(1, "First name is required"),
  lastName: z.string().min(1, "Last name is required"),
});

type FormValues = z.infer<typeof formSchema>;

interface PreCallScreenProps {
  interview: Interview;
  isLoading: boolean;
  interviewTimeDuration: string;
  interviewerImg: string;
  onStart: (email: string, name: string) => void;
  onExit: () => void;
}

export function PreCallScreen(Props: PreCallScreenProps) {
  const { interview, isLoading, interviewTimeDuration, interviewerImg, onStart, onExit } = Props;

  const form = useForm<FormValues>({
    resolver: zodResolver(formSchema),
    mode: "onChange",
    defaultValues: { email: "", firstName: "", lastName: "" },
  });

  const { control, formState } = form;

  const onSubmit = (values: FormValues) => {
    onStart(values.email, `${values.firstName} ${values.lastName}`);
  };

  return (
    <div className="w-full max-w-4xl py-8">
      <Card className="w-full overflow-hidden rounded-md border-2 border-border border-r-4 border-b-4 bg-card">
        {/* Card header */}
        <div className="border-border border-b p-6 text-center">
          <h1 className="mb-1 font-bold text-2xl">ðŸ‘‹ Ready to Start Your Interview?</h1>
          <p className="text-muted-foreground text-sm">{interview.name}</p>
          <div className="mt-3 flex flex-wrap items-center justify-center gap-4">
            {interview.logoUrl && (
              <div className="flex items-center gap-2">
                <Image
                  src={interview.logoUrl}
                  alt="Company Logo"
                  width={24}
                  height={24}
                  className="h-6 w-auto object-contain"
                />
                <span className="font-medium text-sm">{interview.name}</span>
              </div>
            )}
            <div className="flex items-center gap-1 text-muted-foreground text-sm">
              <AlarmClockIcon className="h-4 w-4" />
              <span className="font-semibold">{interviewTimeDuration} min</span>
              <span>or less</span>
            </div>
          </div>
        </div>

        {/* Two-column body */}
        <div className="grid grid-cols-1 gap-6 p-6 md:grid-cols-2">
          {/* Left panel */}
          <div className="flex flex-col gap-5">
            {/* AI greeting bubble */}
            <div className="flex items-start gap-3">
              <div className="shrink-0">
                {interviewerImg ? (
                  <Image
                    src={interviewerImg}
                    alt="AI Interviewer"
                    width={48}
                    height={48}
                    className="rounded-full object-cover"
                  />
                ) : (
                  <div className="flex h-12 w-12 items-center justify-center rounded-full bg-primary/10">
                    <span className="text-lg text-primary">AI</span>
                  </div>
                )}
              </div>
              <div className="rounded-2xl rounded-tl-none bg-muted px-4 py-3 text-foreground text-sm">
                Hi, I&apos;m your AI interviewer. I&apos;ll be asking you a few short questions
                about your experience, skills, and how you solve problems. Ready?
              </div>
            </div>

            {/* Before We Begin */}
            <div>
              <p className="mb-2 font-semibold text-sm">Before We Begin</p>
              <div className="flex flex-wrap gap-2">
                <span className="flex items-center gap-1 rounded-full bg-muted px-3 py-1 font-medium text-xs">
                  ðŸŽ¤ Make sure your mic is on
                </span>
                <span className="flex items-center gap-1 rounded-full bg-muted px-3 py-1 font-medium text-xs">
                  ðŸŽ§ Sit in a quiet space
                </span>
                <span className="flex items-center gap-1 rounded-full bg-muted px-3 py-1 font-medium text-xs">
                  ðŸ”´ Tab switching will be recorded
                </span>
              </div>
            </div>

            <p className="text-muted-foreground text-xs">
              Don&apos;t worry â€” we&apos;ll guide you every step of the way. By continuing
              you&apos;re agreeing to our terms.
            </p>
          </div>

          {/* Right panel â€” form */}
          <form className="flex flex-col gap-4" onSubmit={form.handleSubmit(onSubmit)}>
            {!interview?.isAnonymous && (
              <FieldGroup>
                <div>
                  <p className="mb-2 font-medium text-sm">Enter your name</p>
                  <div className="grid grid-cols-2 gap-3">
                    <Controller
                      control={control}
                      name="firstName"
                      render={({ field, fieldState }) => (
                        <Field data-invalid={fieldState.invalid}>
                          <FieldContent>
                            <Input
                              {...field}
                              id="firstName"
                              placeholder="First Name"
                              aria-invalid={fieldState.invalid}
                              className="font-normal text-sm"
                            />
                          </FieldContent>
                          {fieldState.invalid && <FieldError errors={[fieldState.error]} />}
                        </Field>
                      )}
                    />

                    <Controller
                      control={control}
                      name="lastName"
                      render={({ field, fieldState }) => (
                        <Field data-invalid={fieldState.invalid}>
                          <FieldContent>
                            <Input
                              {...field}
                              id="lastName"
                              placeholder="Last Name"
                              aria-invalid={fieldState.invalid}
                              className="font-normal text-sm"
                            />
                          </FieldContent>
                          {fieldState.invalid && <FieldError errors={[fieldState.error]} />}
                        </Field>
                      )}
                    />
                  </div>
                </div>

                <div>
                  <p className="mb-2 font-medium text-sm">Enter your email address</p>
                  <Controller
                    control={control}
                    name="email"
                    render={({ field, fieldState }) => (
                      <Field data-invalid={fieldState.invalid}>
                        <FieldContent>
                          <Input
                            {...field}
                            id="email"
                            placeholder="Enter your email address"
                            aria-invalid={fieldState.invalid}
                            className="font-normal text-sm"
                          />
                        </FieldContent>
                        {fieldState.invalid && <FieldError errors={[fieldState.error]} />}
                      </Field>
                    )}
                  />
                </div>
              </FieldGroup>
            )}

            <Button
              type="submit"
              className="h-10 w-full"
              disabled={isLoading || (!interview?.isAnonymous && !formState.isValid)}
            >
              {!isLoading ? "I'm Ready" : <MiniLoader />}
            </Button>

            <ConfirmDialog
              trigger={
                <Button
                  type="button"
                  variant="secondary"
                  className="h-10 w-full text-muted-foreground"
                  disabled={isLoading}
                >
                  Exit
                </Button>
              }
              title="Are you sure you want to exit the interview?"
              onConfirm={onExit}
            />
          </form>
        </div>

        {/* Footer */}
        <div className="flex items-center justify-center border-border border-t pt-6 text-center">
          <div className="text-center font-semibold text-sm">
            Powered by{" "}
            <span className="font-bold">
              Open<span className="text-primary">Hire</span>
            </span>
          </div>
        </div>
      </Card>
    </div>
  );
}
